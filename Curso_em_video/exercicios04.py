D = input("digite algo \n")

print(type(D))

print(f"É alfanumerico? {D.isalnum()}")
print(f"É numérico? {D.isnumeric()}")
print(f"É letra? {D.isalpha()}")
print(f"É decimal? {D.isdecimal()}")
print(f"É um espaço? {D.isspace()}")
print(f"É maiúsculo? {D.istitle()}")
print(f"É minusculo? {D.islower()}")
print(f"É capitalizado(titulo)?{D.istitle()}")
